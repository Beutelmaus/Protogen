void drawSmile1(uint8_t r, uint8_t g, uint8_t b) {
  for (int y = 0; y < 32; y++) {
    for (int x = 0; x < 64; x++) {
      if (smiley_full[y][x]) {
        dma_display->drawPixelRGB888(x, y, r, g, b); // use passed color
      } else {
        dma_display->drawPixelRGB888(x, y, 0, 0, 0); // black background
      }
    }
  }
}